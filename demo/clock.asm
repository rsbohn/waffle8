/ a simple clock
/ uses the RTC value at 07760
		* 0000
		JMP I 0002
		HLT
		0200

		* 0200
		CLA CLL
		TAD I PRTC
		DCA TIME_NOW
		TAD TIME_NOW
		JMS DIV10
		JMS DIV3
		JMS DIV2
		DCA HOURS
		TAD TIME_NOW
		JMS MOD60
		DCA MINS

		HLT
/ OR
		CLA CLL		/ alternate
		TAD I PRTC
		DCA TIME_NOW
		TAD TIME_NOW
		JMS DIV5
		JMS DIV3
		JMS DIV2
		JMS DIV2
		DCA HOURS
		TAD TIME_NOW
		JMS MOD60
		DCA MINS

		TAD TIME_NOW
		HLT
/ -------- variables -------
PRTC,		07760		/ PRTC
TIME_NOW,	0		/ TIME NOW
HOURS,		0		/ HOURS
MINS,		0		/ MINUTES


DIV2,		0
		CLL RAR
		JMP I DIV2

DIV3,		0
		DCA VALUE
		DCA QUOT
D3LOOP,		CLA CLL
		TAD VALUE
		TAD M3
		SPA
		JMP D3DONE
		DCA VALUE
		ISZ QUOT
		JMP D3LOOP
D3DONE,		CLA CLL
		TAD QUOT
		JMP I DIV3

QUOT,		0
VALUE,		0
TEMP,		0
M3,		07775
M5,		07773
DIV5,		0
		DCA TEMP
		DCA QUOT
D5LOOP,		CLA CLL
		TAD TEMP
		TAD M5
		SPA
		JMP D5DONE
		DCA TEMP
		ISZ QUOT
		JMP D5LOOP
D5DONE,		CLA CLL
		TAD QUOT
		JMP I DIV5

DIV10,		0
		JMS DIV5
		JMS DIV2
		JMP I DIV10

MOD60,		0
M60A,		TAD M60
		SMA 			/ go until AC is < 0
		JMP M60A
		TAD P60
		JMP I MOD60

M60,		07704
P60,		00074
	
		
		
